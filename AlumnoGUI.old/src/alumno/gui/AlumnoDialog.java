//crea el metodo mostrar que se usa en los botones agregar, modificar y consultar
    Alumno mostrar(Alumno alu, boolean isUpdate) throws PersonaException {
        //si alumno nes null, lo crea
        if (alu==null) {
        //si es null lo crea para avitar nu nullpointer exception
            alu = new Alumno();
        }
        else {
        //llama al metodo que setea los textField tomandolos de alu con los get
            alu2Form(alu);
        //llama al metodo que habilita o deshabilita fecNacDateChooser dependiendo de la acci√≥n
            habilitarDeshabilitar(isUpdate);
        }
        //carga en aluDto al alu
        aluDTO=alu;
        // Se Supende la ejecuci√≥n para mostrar los datos. Se libera cuando se cierra el di√°logo (setVisible(false))
        setVisible(true);
        
        // se cerr√≥ el di√°logo
        if (!isSave) {
            return null;
        }
        //llama al metodo que carga a alu con los textfield
        form2Alu();
        return this.aluDTO;
    }
//metodo que carga al Alumno aluDTO con los textfield
    private void form2Alu() throws NumberFormatException, PersonaException {
        aluDTO.setDni(Integer.valueOf(numDocTextField.getText()));
        aluDTO.setApyNom(apynTextField.getText());
        //para tomar setFechaNac crea un MiCalendario con el metodo creado.
        aluDTO.setFechaNac(new MiCalendario(fecNacDateChooser.getCalendar()));
        //el string seleccionado (f - m) del combobox lo pasa a caracter
        aluDTO.setSexo(sexoComboBox.getSelectedItem().toString().toUpperCase().charAt(0));
        aluDTO.setCantMatAprob(Integer.valueOf(cantMatAprobTextField.getText()));
        aluDTO.setFechaIng(new MiCalendario(fecIngDateChooser.getCalendar()));
        aluDTO.setPromedio(Double.valueOf(promTextField.getText()));  
        //.charAt(0) transforma getText del TextField en char
        aluDTO.setEstado(estadoTextField.getText().charAt(0));
        //aluDTO.setEstado('A');
    }

    private void alu2Form(Alumno alu) {
        //setea los textField del dialog tomandolos de los get de la base
        numDocTextField.setText(String.valueOf(alu.getDni()));
        apynTextField.setText(alu.getApyNom());
        fecNacDateChooser.setCalendar(alu.getFechaNac());
    //para cargar el combobox que solo puede ser 0 o 1 usa un if en el get, si es f pone en 0 si no en 1
        sexoComboBox.setSelectedIndex(alu.getSexo()=='f'?0:1);
        cantMatAprobTextField.setText(String.valueOf(alu.getCantMatAprob()));
        fecIngDateChooser.setCalendar(alu.getFechaIng());
        estadoTextField.setText(String.valueOf(alu.getEstado()));
        promTextField.setText(String.valueOf(alu.getPromedio()));
    }

    private void habilitarDeshabilitar(boolean isUpdate) {
        //deshabilita o habilita fecNacDateChooser dependiendo de la acci√≥n
        numDocTextField.setEnabled(false);
        apynTextField.setEnabled(isUpdate);
        fecNacDateChooser.setEnabled(isUpdate);
        sexoComboBox.setEnabled(isUpdate);
        cantMatAprobTextField.setEnabled(isUpdate);
        fecIngDateChooser.setEnabled(isUpdate);
        estadoTextField.setEnabled(isUpdate);
        promTextField.setEnabled(isUpdate);
        saveButton.setEnabled(isUpdate);
    }
    
    
