    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        try {
            //Si el documeno es null o vacio da un mensaje y vuelve
            if(numDocTextField.getText() == null || "".equals(numDocTextField.getText()))
                {
                    JOptionPane.showMessageDialog(this, "El DNI no puede estar vacio.","Error de DNI",JOptionPane.ERROR_MESSAGE);
                    return;
                }
        //llama al metodo form2Alu que carga alu con los textField
            form2Alu();
        } catch (PersonaException ex) {
            Logger.getLogger(AlumnoDialog.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Error: " + ex,"Error en la Creacion",JOptionPane.ERROR_MESSAGE);
            return;
        }     
//marca en true la variable isSave y le quita visibilidad al boton guardar
        isSave = true;
        setVisible(false);        
    }//GEN-LAST:event_saveButtonActionPerformed

